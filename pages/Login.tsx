import React, { useEffect } from 'react';
import { Link, Navigate, useLocation } from 'react-router-dom';
import { useAuth } from '../context/AuthContext';
import Button from '../components/Button';
import Logo from '../components/Logo';

const Login: React.FC = () => {
  const { loginWithGoogle, user, isLoading } = useAuth();
  const location = useLocation();
  const returnTo = location.state?.returnTo;

  useEffect(() => {
    window.scrollTo(0, 0);
  }, []);

  if (user) {
    return <Navigate to="/dashboard" />;
  }

  const handleGoogleAuth = async () => {
    if (returnTo) {
      localStorage.setItem('authReturnTo', returnTo);
    }
    await loginWithGoogle();
  };

  return (
    <div className="flex min-h-[80vh] items-center justify-center px-4 sm:px-6 lg:px-8 py-12">
      <div className="w-full max-w-md space-y-8 bg-white dark:bg-slate-900 p-8 rounded-2xl border border-slate-200 dark:border-slate-800 shadow-2xl relative overflow-hidden">
        {/* Decorative blur */}
        <div className="absolute top-0 left-1/2 -translate-x-1/2 w-full h-2 bg-gradient-to-r from-sky-400 to-sky-600"></div>

        <div className="text-center">
          <div className="mx-auto h-24 w-24 rounded-full bg-white p-2 flex items-center justify-center mb-6 shadow-2xl ring-4 ring-sky-500/20">
            <Logo className="h-full w-full" alt="Organization Logo" />
          </div>
          <h2 className="text-3xl font-bold tracking-tight text-slate-900 dark:text-white">
            Welcome
          </h2>
          <p className="mt-2 text-sm text-slate-600 dark:text-slate-400">
            Sign in or create an account to manage events, donations, and more.
          </p>
        </div>

        <div className="mt-8 space-y-6">
          <div className="flex flex-col gap-3">
            <Button
              type="button"
              variant="outline"
              fullWidth
              onClick={handleGoogleAuth}
              disabled={isLoading}
              className="relative flex items-center justify-center gap-3 bg-white hover:bg-slate-50 border-slate-300 text-slate-900 shadow-sm google-login-btn font-semibold"
            >
              <svg className="h-5 w-5" viewBox="0 0 24 24">
                <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4" />
                <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853" />
                <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.26.81-.58z" fill="#FBBC05" />
                <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335" />
              </svg>
              {isLoading ? 'Connecting...' : 'Continue with Google'}
            </Button>
          </div>
        </div>
      </div>
    </div>
  );
};

export default Login;